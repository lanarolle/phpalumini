                  <p>
                    Our sole aim and objective are to make the Nāgānanda International Institute for Buddhist Studies
                    (NIIBS) which is a role model of Nalanda and Jagandala Universities and that represents the
                    Mahavihara Bhikkhu generation the centre of excellence for modern International Buddhist studies.
                    Established in the recent past, this institute has made headway in International University Ranking
                    within a relatively short period of time. <br><br>

                    This institute, fully equipped with human resources needed for both local and foreign students for
                    the pursuit and exploration of Dhamma studies both in Sinhala and English is blessed with a mass
                    wealth of physical resources which any other institute in this country cannot claim. The students
                    enrolled here study for their B.A. (General), B.A. (Special), BSc (Hons), M.A, M.Phil.and Ph.D.
                    Degrees with keen enthusiasm.<br><br>

                    Without being confined to mere Buddhist theoretical studies in the field of Buddhist Education, this
                    institute is supposed to create Buddhist leaders and doyens needed for the modern world and to
                    provide solutions found in Buddhism to the social conflicts, issues and problems prevalent in the
                    modern society by expanding its horizons of practical, empirical and experimental domain. I warmly
                    welcome you all and wish you all the best and good luck and every success in all your future
                    endeavors.<br><br>

                  <p>Ven. Dr. Bodagama Chandima Thero <br>
                    Vice Chancellor / Executive Director</p>

                  </p>
                </div>
              </li>

              <li>
