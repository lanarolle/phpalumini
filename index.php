              <li>
                <a data-toggle="collapse" class="collapsed" href="#faq2">Our Mission?

                  <i class="fa fa-minus-circle"></i></a>
                <div id="faq2" class="collapse" data-parent="#faq-list">
                  <p>
                    Our mission is the discovery, preservation and dissemination of knowledge rooted in Buddhist
