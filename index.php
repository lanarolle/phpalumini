              </li>

              <li>
                <a data-toggle="collapse" class="collapsed" href="#faq3">History?
                  <i class="fa fa-minus-circle"></i></a>
                <div id="faq3" class="collapse" data-parent="#faq-list">
                  <p>
                    NIIBS established on the premises of the Manelwatta temple is the 10th International Buddhist
                    University in the world and the first of its kind after 800 years of Buddhist educational history.
                    During the period from the first century to the 13th century there were nine International Buddhist
                    Universities in the world. NIIBS was established during the 21st century, after a lull period of 800
                    years.



                    This 40 acres of picturesque land where Manelwatta Temple is situated was gifted to the Buddha
                    Sasana by then President of the Democratic Socialist Republic of Sri Lanka, the late Mr. J. R.
                    Jayewardene and his wife Mrs. Elena Jayewardene for the express purpose of providing facilities to
                    learn Dhamma and live a disciplined life in keeping with the teachings of the Buddha. According to
                    historical documents, this land area earlier known as “Rajawatta” is located in the area which was
                    gifted to the Buddha by Naga king Maniakkitha during the Buddha’s visit to “Kelanipura”. <br><br>

                    This land, first gifted to Welivita Sangaraja temple where the then Mahanayaka Thera of the Malwatta
                    chapter resided, was allocated to Ven. Kandakkaliye Dhammakiththi Nayaka thera who was appointed as
                    the Chief Incumbent of the Manelwatta temple, who initiated the development activities of the
                    temple. When Dhammakiththi Thera was unable to continue his service due to his illness, as
                    recommended by Ven. Kirinde Sri Dhammananda Nayaka Thera, the then Chief Incumbent of ‘Sri Lanka
                    Maha Viharaya’ in Malaysia, Ven. Dr. Bodagama Chandima Thera, Chief Sangha Nayaka in Taiwan, was
                    appointed as the Chief Incumbent of the Manelwatta temple in 2002 and was given the responsibility
                    of developing it to achieve the objective of this donation. Due to the commitment, dedication and
                    leadership of the Ven. Dr. Bodagama Chandima Thera it was possible to establish, among others, an
                    academy to provide short-term programmes in Buddhist Studies, a Bikkuni Training Institute, and an
                    Āyurvedic hospital. As the first step of establishing an International Buddhist University, the
                    NIIBS was established in August 2013. The Institute gained the acceptance of the Ministry of Higher
                    Education, Sri Lanka in December 2015. <br><br>

                    It should also be stated that the unique feature of the Stupa located in the Manelwatta temple is
                    that the relics are deposited at three places of the Stupa. Relics of Seevali Maha Thera are
                    deposited near “Pesa Walalu”, relics of the Buddha which had been, at one time, in the possession of
                    King Sri Wickrama Rajasinghe, later was received by late Mr J R Jayewardene through Sangaraja
                    Viharaya – inside the Sthupa and relics of the Buddha donated by the then President of India to late
                    President J. R. Jayawardena at the “Hathares Kotuwa”.
                  </p>
                </div>
              </li>
